public class SyntaxTypesVariables {

    public static void main (String[] args) {
        // primitive data types
        // not objects
        // store values of a specific types: byte, short, int, long, float, double, char, boolean


        // Declaring variables in JAVA --------------------------------------------------------
        // 1. Declare data type before assigning it

        int myFavoriteNumber;
        myFavoriteNumber = 24;
        System.out.println(myFavoriteNumber);

        // storing a letter
        char myFavoriteLetter = 'L';
        System.out.println(myFavoriteLetter);

        // storing a boolean
        boolean isSunny = true;
        System.out.println(isSunny);

       // ----------------------------------------------------------------------------------------
    }
}
